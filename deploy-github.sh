# GitHub Deployment Script
# Run these commands after installing Git

# 1. Initialize repository
git init

# 2. Add all files
git add .

# 3. Create first commit
git commit -m "Initial commit: Wearable Timeline App"

# 4. Create GitHub repository (manual step):
#    - Go to https://github.com/new
#    - Name: WearableOverlayTimeline
#    - Click "Create repository"

# 5. Connect to GitHub (replace YOUR_USERNAME with your GitHub username)
# git remote add origin https://github.com/YOUR_USERNAME/WearableOverlayTimeline.git

# 6. Push to GitHub
# git branch -M main
# git push -u origin main

# 7. Deploy on Vercel:
#    - Go to https://vercel.com/new
#    - Click "Import Git Repository"
#    - Select "WearableOverlayTimeline"
#    - Click "Deploy"

